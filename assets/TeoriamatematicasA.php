    <h1>🧮 Teoría de Matemáticas para Niños 📚</h1>

    <div class="section">
        <h2>1. Sumas ➕</h2>
        <p>La suma es la operación que se usa para encontrar el total al juntar dos o más números.</p>
        <div class="example">
            <p><strong>Ejemplo:</strong> Si tienes 3 manzanas 🍏 y tu amigo te da 2 manzanas 🍎, ¿cuántas manzanas tienes ahora?</p>
            <p><strong>Respuesta:</strong> 3 + 2 = 5 🍏🍏🍏🍎🍎</p>
        </div>
    </div>

    <div class="section">
        <h2>2. Restas ➖</h2>
        <p>La resta es la operación que se usa para encontrar cuánto queda al quitar un número de otro.</p>
        <div class="example">
            <p><strong>Ejemplo:</strong> Si tienes 5 caramelos 🍬 y comes 2, ¿cuántos caramelos te quedan?</p>
            <p><strong>Respuesta:</strong> 5 - 2 = 3 🍬🍬🍬</p>
        </div>
    </div>

    <div class="section">
        <h2>3. Multiplicaciones ✖️</h2>
        <p>La multiplicación es una forma rápida de sumar el mismo número varias veces.</p>
        <div class="example">
            <p><strong>Ejemplo:</strong> Si tienes 4 cajas 📦 con 3 juguetes 🧸 en cada caja, ¿cuántos juguetes tienes en total?</p>
            <p><strong>Respuesta:</strong> 4 × 3 = 12 🧸🧸🧸🧸🧸🧸🧸🧸🧸🧸🧸🧸</p>
        </div>
    </div>

    <div class="section">
        <h2>4. Divisiones ➗</h2>
        <p>La división es la operación que se usa para repartir un número en partes iguales.</p>
        <div class="example">
            <p><strong>Ejemplo:</strong> Si tienes 12 galletas 🍪 y quieres compartirlas entre 3 amigos, ¿cuántas galletas le tocará a cada uno?</p>
            <p><strong>Respuesta:</strong> 12 ÷ 3 = 4 🍪🍪🍪🍪 por amigo.</p>
        </div>
    </div>

    <div class="section">
        <h2>5. Fracciones 🍕</h2>
        <p>Las fracciones se usan para representar una parte de un todo.</p>
        <div class="example">
            <p><strong>Ejemplo:</strong> Si una pizza 🍕 se divide en 4 partes iguales y comes 1 parte, has comido <strong>1/4</strong> de la pizza.</p>
            <p>Si comes 3 partes, queda <strong>1/4</strong> de la pizza.</p>
        </div>
    </div>

