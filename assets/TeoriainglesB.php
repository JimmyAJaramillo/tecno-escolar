    <h2>🎨 1. Preposiciones de lugar (In, On, Under)</h2>
    <p>¡Imagina que tienes un gatito mágico llamado <strong>Whiskers</strong>! 🐱 A veces, Whiskers está <span class="important" data-img="url('https://via.placeholder.com/120?text=On')">sobre</span> tu cama, otras veces <span class="important" data-img="url('https://via.placeholder.com/120?text=In')">dentro</span> de la caja, y cuando juega a las escondidas, está <span class="important" data-img="url('https://via.placeholder.com/120?text=Under')">debajo</span> de la mesa.</p>
    <p>¡Vamos a aprender cómo decir dónde está Whiskers!</p>
    <ul>
      <li><span class="important" data-img="url('https://via.placeholder.com/120?text=In')">In</span>: Cuando algo está dentro de algo. Ejemplo: "The cat is <strong>in</strong> the box." (El gato está dentro de la caja).</li>
      <li><span class="important" data-img="url('https://via.placeholder.com/120?text=On')">On</span>: Cuando algo está sobre algo. Ejemplo: "The book is <strong>on</strong> the table." (El libro está sobre la mesa).</li>
      <li><span class="important" data-img="url('https://via.placeholder.com/120?text=Under')">Under</span>: Cuando algo está debajo de algo. Ejemplo: "The cat is <strong>under</strong> the table." (El gato está debajo de la mesa).</li>
    </ul>
    <p>🔍 <strong>Recuerda</strong>: ¡Whiskers siempre encuentra el mejor lugar para esconderse!</p>
  </div>

