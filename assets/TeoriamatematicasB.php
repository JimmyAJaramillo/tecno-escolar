    <h1>Aprendamos Matemáticas con Diversión! 🎉</h1>

    <h2>1. ¿Qué es la suma? 🤔 ➕</h2>
    <p>Imagina que tienes <strong>5 caramelos</strong> y tu amigo te da <strong>3 más</strong>. Ahora, ¿cuántos caramelos tienes en total? ¡Es hora de <strong>sumar</strong>!</p>
    <div class="example">
        <p>Si tienes 5 caramelos 🍬 y te dan 3 más, lo que haces es sumarlos:</p>
        <p><strong>5 + 3 = 8</strong></p>
        <p>¡Así que ahora tienes <strong>8 caramelos</strong> en total! 🥳</p>
    </div>

    <h2>2. ¿Qué es la resta? 😲 ➖</h2>
    <p>Ahora, imagina que tienes <strong>10 globos</strong>, pero se te escapan <strong>4 globos</strong>. ¿Cuántos globos te quedan? Para saberlo, tienes que <strong>restar</strong>.</p>
    <div class="example">
        <p>Si tienes 10 globos 🎈 y pierdes 4, lo que haces es restar:</p>
        <p><strong>10 - 4 = 6</strong></p>
        <p>¡Te quedan <strong>6 globos</strong> para seguir jugando!</p>
    </div>

    <h2>3. ¿Qué es la multiplicación? 😎 ✖</h2>
    <p>Imagina que tienes <strong>6 cajas de juguetes</strong> y en cada caja hay <strong>7 juguetes</strong>. ¿Cuántos juguetes tienes en total? ¡Puedes usar la <strong>multiplicación</strong> para hacerlo rápido!</p>
    <div class="example">
        <p>Si tienes 6 cajas y en cada una hay 7 juguetes:</p>
        <p><strong>6 × 7 = 42</strong></p>
        <p>¡Así que en total tienes <strong>42 juguetes</strong>! 😲</p>
    </div>

